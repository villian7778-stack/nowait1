/// Supported language codes.
const kLangEn = 'en';
const kLangHi = 'hi';
const kLangMr = 'mr';

class LocaleService extends ChangeNotifier {
  static final LocaleService instance = LocaleService._();
  LocaleService._();

  String _lang = kLangEn;

  String get lang => _lang;

  /// Load persisted language preference at app start.
  Future<void> loadFromStorage() async {
    final prefs = await SharedPreferences.getInstance();
    _lang = prefs.getString('app_language') ?? kLangEn;
  }

  /// Change language and persist. Notifies all listeners so the UI rebuilds.
  Future<void> setLanguage(String lang) async {
    if (_lang == lang) return;
    _lang = lang;
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString('app_language', lang);
    notifyListeners();
  }

  /// Translate a key, optionally replacing `{param}` placeholders.
  String tr(String key, {Map<String, String>? params}) {
    final map = _strings[_lang] ?? _strings[kLangEn]!;
    String val = map[key] ?? _strings[kLangEn]![key] ?? key;
    if (params != null) {
      params.forEach((k, v) => val = val.replaceAll('{$k}', v));
    }
    return val;
  }

